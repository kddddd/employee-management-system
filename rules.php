<head><h1><u>Rules of Online Examination</u></h1></head>
<body bgcolor="aqua">
<ol>
<li><h4>Please be sure to reach the test centre as per the reporting time mentioned in the e-hall ticket. You should be present 
for the pre-test procedure including registration
and on-site orientation.</h4></li>
<li><h4>If for some reason you reach after the on-site orientation has 
begun, you will <mark>not</mark> be permitted to write the test.</h4></li>
<li><h4>You have to <mark>register</mark> your name at the <mark>registration</mark> 
counter at the centre where your e-hall ticket will be authenticated.</h4></li>
<li><h4>It is recommended that you enter the examination hall at 
least <mark>15 minutes</mark> before the start of the test.</h4></li>

<li><h4><mark>Please remember that you are not allowed to carry any material,
printed or written, bits of papers or even any rough pages, 
to the test hall. You are also not permitted to bring calculators, 
slide rules, clark tables, electronic watches with facilities of 
calculators, laptop or palmtop computers, personal stereo systems,
walkie-talkie sets, mobile phones, paging devices or any other 
object/device.</mark></h4></li>

<li><h4>Once you begin your test, you will not be allowed to go outside 
the test hall for the entire duration of the test.</h4></li>
<li><h4>If you leave the hall you will not be permitted to return,
under any circumstances.</h4></li>
<li><h4>We do not encourage family members and or friends of the applicants to wait at the test centre. Please make arrangements 
to meet them elsewhere at the end of the test.</h4></li>
<li><h4>The test hall is monitored using web cameras and closed circuit TV cameras and any disruption / disturbance by an applicant is deemed 
as misconduct and can have dire consequences.</h4></li>
<li><h4>During the test, please ensure you
maintain silence and do not disturb others.</h4></li>
<li><h4>If we find an applicant using unfair means, 
or impersonating someone else, his or her application will be 
cancelled and he or she will be liable to be <mark>debarred</mark> from taking the 
entrance examination permanently or for a specified period,
as decided by the concerned authorities.</h4></li>
<li><h4>At the <mark>end</mark> of the test, applicants must sign the attendance sheet in the presence of the invigilator. The invigilator will also put his/her signature 
in the place provided in the <mark>e-hall ticket.</mark></h4></li>
</ol>
